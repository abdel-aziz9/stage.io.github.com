<div style="margin: auto;text-align: justify-all;width: 95%;display: flex;flex-direction: column;box-shadow: lightseagreen 2px 2px 2px 2px;">
    <p>
La DREFOP est une structure déconcentrée du Ministère de l’Emploi et de la Formation Professionnelle. Décrété du N° 2012/644 au 28 Décembre 2012 portant l’organisation du MINEFOP/. 
</p>
<p>Placé sous l’autorité du Délègue régional, la délégation régionale de l’emploi et de la formation professionnelle est chargée:<br>
 De l’accompagnement des collectivités territoriales décentralisées en matière d’emploi, de formation et d’insertion professionnelle ;<br>
   Du suivi de l’évaluation de l’emploi et du marché de l’emploi de la coordination de moyens mis en œuvre et du suivi des actions relatives au maintien et à la création d’emploi;<br>
   De la coordination et de l’animation des activités de formation professionnelle ;<br>
   Du suivi de la programmation et l’organisation de la formation continue des personnels relevant de son ressort de compétence;<br>
   De l’organisation des rencontres d’information sur la possibilité d’emploi, d’apprentissage et d’action de formation professionnelle<br>
   De la proposition des personnels impliques dans l’organisation des examens relatifs de sa compétence,<br><br>
   De l’organisation du fichier des structures publiques et privée de la formation professionnelle ;
   De l’étude des demandes de visa contrats de travailleurs expatriés, ainsi que de leur transmission, assorties <br>d’avis ;  <br>
   De la transmission de données statistique relatives à la main-d’œuvre ;<br>
   De la supervision et du contrôle des activités des agences régionales des organismes sous tutelles ;<br>
   De la préparation des actes relatifs a la gestion des personnels relevant de son autorité ;<br>
   De la délégation des animateurs pédagogique dans les structures de formation professionnelle ;<br>
   De l’étude assortie d’avis, de demandes d’agrément des entreprises de travail temporaire et de bureaux aux affiches <br>privées de placement ;<br>
   De l’étude et visa des contrats de travail des nationaux dans sa circonscription de compétence
</p>




